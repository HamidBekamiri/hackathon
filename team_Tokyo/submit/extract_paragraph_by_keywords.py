i = 1 # ith
texts = list(data[data.country == 'United States'].text_original)
text = texts[3]
text = split_into_sentences(text)

extracted_paragraphs = [' '.join(text[(ind-5): (ind+6)]) for ind, sen in enumerate(text) if 'goal' in sen]



